def test_get_random_books_by_genre(client, db_session):
    # Add test data
    db_session.add_all([
        Book(title="Dune", author="Frank Herbert", pages=412, genre="Science Fiction"),
        Book(title="Neuromancer", author="William Gibson", pages=271, genre="Science Fiction"),
        Book(title="The Hobbit", author="J.R.R. Tolkien", pages=310, genre="Fantasy"),
        Book(title="Harry Potter and the Sorcerer's Stone", author="J.K. Rowling", pages=309, genre="Fantasy")
    ])
    db_session.commit()
    response = client.get("/books/random-by-genre/")
    assert response.status_code == 200
    data = response.json()
    assert "Science Fiction" in data
    assert len(data["Science Fiction"]) == 2
    assert "Fantasy" in data
    assert len(data["Fantasy"]) == 2
